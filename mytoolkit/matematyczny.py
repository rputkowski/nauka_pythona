# są 2 rodzaje metod: takie które zwracają jakąś wartość (wtedy w ciele metody mamy słowo kluczowe return) albo takie, które tylko wykonują i nie zwracają wartości
# nasz moduł o nazwie pomocniczy na początku będzie zawierał 2 metody: dodaj i odejmij
def dodaj(a,b):
    result = a + b
    return result
def odejmij(a,b):
    result = a - b
    return result
